 plot(data(:,4),data(:,3))
 xlabel('distance (km)')
 ylabel('elevation (m) ')
 title('Bi bideen altuerak')
 legend('a1' , 'n1')
 hold on
  data2 = dlmread (autobidea{1}, ',',1,0);
  subplot(1,2,2)
 plot(data2(:,1),data2(:,2))
 xlabel('longitude')
 ylabel('latitude')
 title('Bideak')
 legend('a1' , 'n1')
 hold on
end
